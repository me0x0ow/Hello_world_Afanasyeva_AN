#!/bin/bash

echo "--- Анализ использования диска ---"
printf "%-25s %-10s %s\n" "Файловая система" "Занято" "Статус"
echo "--------------------------------------------------------"

df -h | awk 'NR > 1 {
    usage = $5
    gsub(/%/, "", usage)
    status = ""
    if (usage > 90) {
        status = "!!! ВНИМАНИЕ: ПЕРЕПОЛНЕНО !!!"
    }

    printf "%-25s %-10s %s\n", $1, $5, status
}'
